export const productCatalog = {};

export function hydrateProductCatalog(products) {
  Object.keys(productCatalog).forEach((key) => delete productCatalog[key]);
  products.filter((item) => item.kind === 'product').forEach((item) => {
    productCatalog[item.normalized_name] = {
      id: item.id,
      category: item.department,
      quantity: Number(item.quantity),
      unit: item.unit,
      icon: item.icon
    };
  });
}
